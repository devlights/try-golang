package logging

import "github.com/devlights/try-golang/mapping"

type (
	register struct{}
)

// NewRegister -- このパッケージ用のサンプルを登録する mapping.Register を生成します。
func NewRegister() mapping.Register {
	return new(register)
}

// Regist -- 登録します.
func (r *register) Regist(m mapping.ExampleMapping) {
	m["log_flags"] = Flags
	m["log_prefix"] = Prefix
	m["log_output"] = Output
	m["log_new"] = NewLogger
}
